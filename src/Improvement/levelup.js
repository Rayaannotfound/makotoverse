function LevelingUp(Experience, OriginalLevel) {
  for (const [level, exp] of Object.entries(Levels)) {
    if (Experience > exp[0] - 1 && Experience < exp[1] - 1) {
      
      if (OriginalLevel < level) {
        
        return [level, true];
      } else {
       
        return 0;
      }
    }
  }
}

function getNextLevel(level, xp) {
  const next = Levels[level][1] - xp;
  return next;
}

function getDifference(level) {
  const distance = Levels[level];
  return distance;
}
